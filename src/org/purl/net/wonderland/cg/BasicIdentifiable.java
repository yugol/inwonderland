package org.purl.net.wonderland.cg;

import org.purl.net.wonderland.util.IdUtil;

/**
 *
 * @author Iulian Goriac <iulian.goriac@gmail.com>
 */
public abstract class BasicIdentifiable {

    private final String id;
    private Object tag = null;

    public BasicIdentifiable() {
        this.id = IdUtil.newId();
    }

    public BasicIdentifiable(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public Object getTag() {
        return tag;
    }

    public void setTag(Object tag) {
        this.tag = tag;
    }

    @Override
    public int hashCode() {
        return id.hashCode();
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final BasicIdentifiable other = (BasicIdentifiable) obj;
        if ((this.id == null) ? (other.id != null) : !this.id.equals(other.id)) {
            return false;
        }
        return true;
    }
}
